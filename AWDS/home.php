        <h4 id="hdr">Some highlights about Indian forests</h4><br>
        <p id="sa">India is not only famous for its diverse wildlife, architectural marvels and culture, but also for its dense and vast forest cover.<br> Indian climate befits the variety of flora and fauna.Forest is the second largest land use in India next to agriculture.<br> The forest cover of India is assessed as 67.83 million hectares which constitute 20.64 per cent of the country's<br> geographical area, ranging from the Himalayan Temperate to Dry Zone forests. The National Forest<br> Policy stipulates that one-third of area should be under forest or tree cover. Being a<br> mega-bio diversity country, the nation possesses high level of endemism.</p><br>
        <p id="sa">The forests play vital role in harboring more than 45,000 floral and 81,000 faunal species of which 5150 floral and 1837 faunal <br>species are endemic. The nation has established 597 Protected Areas comprising 95 National Parks, 500 Wildlife Sanctuaries,<br> 2 conservation reserves covering 1.56 million ha area or 4.75 per cent geographical area of the country. </p><br><br><br>

        
